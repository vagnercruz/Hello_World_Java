public class Program {

    public static void main(String[] args) {
//      Tipo       nome do objeto          Nome da Classe
        HelloWorld helloWorld        = new HelloWorld();
        helloWorld.sayHello();
    }
}


/*
    O tipo vai sempre ter o nome da classe

    O nome do objeto pode ser qualquer um, mas eu decidir por o  mesmo da classe porém seguindo o camel case (a primeira minuscula)

    = é o sinal de atribuição

    HelloWorld() é o nome da classe, ou seja estamos chamando o constructor
*/